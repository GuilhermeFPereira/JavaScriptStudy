/* 

Operadores de Comparacao

==   -> Igual // Compara o valor, mas nao o tipo
===  -> TOTALMENTE igual // Compara o VALOR e o TIPO

!=   -> Diferente // Compara o valor, mas nao o tipo
!==  -> TOTALMENTE Diferente // Compara o VALOR e o TIPO

>  --> Maior
<  --> Menor

>=  -> Maior ou igual
<=  -> Menor ou igual

*/